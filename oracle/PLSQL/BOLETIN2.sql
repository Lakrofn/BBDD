CREATE OR REPLACE PROCEDURE subir_salario
AS
	CURSOR c_empleados IS
	SELECT e.NUMEM, e.NOMEM, e.SALAR
	FROM EMPLEADOS e
	WHERE e.NUMHI > 2 AND e.SALAR < 2000;
	v_salariofinal NUMBER;
BEGIN
	FOR registro IN c_empleados
	LOOP
	v_salariofinal := registro.SALAR * 1.10;
	UPDATE EMPLEADOS e
	SET e.SALAR = registro.SALAR
	WHERE e.NUMEM = registro.NUMEM;
	DBMS_OUTPUT.PUT_LINE('Código empleado; ' || registro.NUMEM);
	DBMS_OUTPUT.PUT_LINE('Nombre empleado; ' || registro.NOMEM);
	DBMS_OUTPUT.PUT_LINE('Salario anterior; ' || registro.SALAR);
	DBMS_OUTPUT.PUT_LINE('Salario nuevo; ' || v_salariofinal);
	DBMS_OUTPUT.PUT_LINE('-----------');
	END LOOP;
END;

BEGIN
	subir_salario;
END;

