CREATE OR REPLACE PROCEDURE info
AS
	NUM_EMPLEADOS NUMBER;
	MIN_SALARIO NUMBER;
	MAX_SALARIO NUMBER;
	AVG_SALARIO NUMBER;
BEGIN
	SELECT COUNT(e.NUMDE), MIN(e.SALAR), MAX(e.SALAR), AVG(e.SALAR)
	INTO NUM_EMPLEADOS, MIN_SALARIO, MAX_SALARIO, AVG_SALARIO
	FROM DEPARTAMENTOS d
	JOIN EMPLEADOS e
	ON d.NUMDE = e.NUMDE
	WHERE d.NOMDE = 'FINANZAS';

	DBMS_OUTPUT.PUT_LINE ('Empleados trabajando: ' || NUM_EMPLEADOS);
	DBMS_OUTPUT.PUT_LINE ('Salario minimo: ' || MIN_SALARIO);
	DBMS_OUTPUT.PUT_LINE ('Salario maximo: ' || MAX_SALARIO);
	DBMS_OUTPUT.PUT_LINE ('Salario medio: ' || AVG_SALARIO);
END;

BEGIN
	info;
END;

