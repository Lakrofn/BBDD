CREATE OR REPLACE PROCEDURE ALVARO.EDAD_EMP(NOM VARCHAR2)
AS
	FECHA DATE;
	V_ANIOS NUMBER;
	V_MESES NUMBER;
	V_DIAS NUMBER;
BEGIN
	SELECT FECNA INTO FECHA FROM EMPLEADOS
	WHERE UPPER(NOMEM) = UPPER(NOM);
	
	V_ANIOS := TRUNC(MONTHS_BETWEEN(SYSDATE, FECHA) / 12);
	V_MESES := TRUNC(MOD(MONTHS_BETWEEN(SYSDATE, FECHA), 12));
	V_DIAS := TRUNC(SYSDATE - ADD_MONTHS(FECHA, TRUNC(MONTHS_BETWEEN(SYSDATE, FECHA))));
	
	DBMS_OUTPUT.PUT_LINE(FECHA);
	DBMS_OUTPUT.PUT_LINE(V_ANIOS);
	DBMS_OUTPUT.PUT_LINE(V_MESES);
	DBMS_OUTPUT.PUT_LINE(V_DIAS);
END;

BEGIN
	EDAD_EMP('CESAR');
END;

