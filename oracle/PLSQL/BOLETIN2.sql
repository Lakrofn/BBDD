CREATE OR REPLACE PROCEDURE ALVARO.CONT_EMPLEADOS(NUM_DEP NUMBER, NUM_HIJOS NUMBER)
AS
	CURSOR NUM_EMPLEADOS(v_dep NUMBER, v_hijos NUMBER) IS
	SELECT NOMEM FROM EMPLEADOS
	WHERE NUMDE = v_dep AND NUMHI = v_hijos;

	v_contador NUMBER := 0;
BEGIN
	FOR empleados IN NUM_EMPLEADOS(NUM_DEP, NUM_HIJOS)
	LOOP
		v_contador := v_contador + 1;
		
	END LOOP;
	
	DBMS_OUTPUT.PUT_LINE(v_contador);
END;

BEGIN
	CONT_EMPLEADOS(121, 3);
END;

