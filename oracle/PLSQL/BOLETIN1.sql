-- 5
CREATE OR REPLACE FUNCTION LAKRO.NOTA(NOTA NUMBER)
RETURN VARCHAR2
IS
BEGIN
	IF NOTA < 5 THEN
	DBMS_OUTPUT.PUT_LINE('INSUFICIENTE');
	ELSIF NOTA = 5 THEN
	DBMS_OUTPUT.PUT_LINE('SUFICIENTE');
	ELSIF NOTA = 6 OR NOTA = 7 THEN 
	DBMS_OUTPUT.PUT_LINE('BIEN');
	ELSIF NOTA = 8 OR NOTA = 9 THEN
	DBMS_OUTPUT.PUT_LINE('NOTABLE');
	ELSIF NOTA = 10 THEN
	DBMS_OUTPUT.PUT_LINE('SOBRSALIENTE');
	ELSE
	DBMS_OUTPUT.PUT_LINE('NOTA INCORRECTA');
	END IF;
END;

SELECT NOTA(5) FROM DUAL;

