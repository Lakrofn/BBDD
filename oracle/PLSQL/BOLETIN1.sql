-- 3
CREATE OR REPLACE PROCEDURE LAKRO.SERIE(MINIMO NUMBER, MAXIMO NUMBER, PASO NUMBER)
AS
	ACTUAL NUMBER(10) := MINIMO;
BEGIN
	WHILE ACTUAL <= MAXIMO LOOP
	DBMS_OUTPUT.PUT_LINE(ACTUAL);
	ACTUAL := ACTUAL + PASO;
	END LOOP;
END;

BEGIN
	SERIE(0,10,2);
END;

