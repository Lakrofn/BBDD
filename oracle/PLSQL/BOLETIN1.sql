CREATE OR REPLACE FUNCTION LAKRO.AZAR(MINIMO NUMBER, MAXIMO NUMBER)
RETURN NUMBER
IS
BEGIN
	RETURN TRUNC(DBMS_RANDOM.VALUE(MINIMO, MAXIMO));
END;

