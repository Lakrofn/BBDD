CREATE OR REPLACE PROCEDURE EMPRESA.primero (dep NUMBER)
AS
	V_NOM VARCHAR2(14);
	V_LOC VARCHAR2(13);
BEGIN
	SELECT DNAME, LOC INTO V_NOM, V_LOC
	FROM DEPT WHERE DEPTNO = dep;

	DBMS_OUTPUT.PUT_LINE('DEP: ' || V_NOM);
	DBMS_OUTPUT.PUT_LINE('UBICADO EN: ' || V_LOC);
	
	EXCEPTION
	WHEN NO_DATA_FOUND THEN
		DBMS_OUTPUT.PUT_LINE('Departamento no existe');
END;


BEGIN
	primero(1);
END;

