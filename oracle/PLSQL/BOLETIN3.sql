CREATE OR REPLACE PROCEDURE TODOSDEP(DEP NUMBER)
IS
	CURSOR EMPLEADOS IS
	SELECT ENAME FROM EMP
	WHERE DEPTNO = DEP;
BEGIN
	FOR EMPLEADO IN EMPLEADOS
	LOOP
		DBMS_OUTPUT.PUT_LINE(EMPLEADO.ENAME);
	END LOOP;
END;

BEGIN
	TODOSDEP(10);
END;

