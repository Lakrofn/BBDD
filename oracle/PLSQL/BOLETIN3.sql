CREATE OR REPLACE FUNCTION SALARIOS(DEP NUMBER)
RETURN NUMBER
IS
	V_SUM NUMBER := 0;
	CURSOR EMPLEADOS IS
	SELECT SAL FROM EMP
	WHERE DEPTNO = DEP;
BEGIN
	FOR EMPLEADO IN EMPLEADOS
	LOOP
		V_SUM := V_SUM + EMPLEADO;
	END LOOP;

	DBMS_OUTPUT.PUT_LINE(V_SUM);
END;

SELECT SALARIOS(10) FROM DUAL;

