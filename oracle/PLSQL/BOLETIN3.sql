CREATE OR REPLACE PROCEDURE MOSTRARABREVIATURAS
AS
	CURSOR EMPLEADOS IS
	SELECT ENAME FROM EMP;
	
BEGIN
	FOR EMPLEADO IN EMPLEADOS
	LOOP
		DBMS_OUTPUT.PUT_LINE(SUBSTR(EMPLEADO.ENAME,1,3));
	END LOOP;
END;

BEGIN
	MOSTRARABREVIATURAS;
END;

