SELECT a.CODART , SUM(lf.CANT) , a.DESCRIP FROM LINEAS_FAC lf 
JOIN ARTICULOS a 
ON lf.CODART = a.CODART
WHERE a.CODART LIKE 'P%'
GROUP BY a.CODART, a.DESCRIP;

-- 21
SELECT a.CODART , SUM(lf.CANT) , a.DESCRIP, MAX(a.PRECIO) - MIN(A.PRECIO) DIFERENCIA FROM LINEAS_FAC lf 
JOIN ARTICULOS a 
ON lf.CODART = a.CODART
WHERE a.CODART LIKE 'P%'
GROUP BY a.CODART, a.DESCRIP;

-- 22
SELECT a.DESCRIP, SUM(lf.PRECIO * lf.CANT) FROM ARTICULOS a 
JOIN LINEAS_FAC lf 
ON a.CODART = lf.CODART
GROUP BY a.DESCRIP 
HAVING SUM(lf.PRECIO * lf.CANT) > 10000;

