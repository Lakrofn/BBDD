-- 19
SELECT p.NOMBRE, COUNT(c.CODPUE) FROM CLIENTES c
JOIN PUEBLOS p
ON c.CODPUE = p.CODPUE
GROUP BY p.NOMBRE
HAVING COUNT(c.CODPUE) > 2
ORDER BY COUNT(c.CODPUE) DESC;

