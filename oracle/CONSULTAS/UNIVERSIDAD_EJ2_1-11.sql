-- 13
SELECT a.NOMBRE ASIGNATURA, a.COSTEBASICO, P.NOMBRE PERSONA_MATRICULADA FROM ASIGNATURA a JOIN ALUMNO_ASIGNATURA aa 
ON a.IDASIGNATURA = aa.IDASIGNATURA
JOIN ALUMNO a2 
ON aa.IDALUMNO = a2.IDALUMNO
JOIN PERSONA p 
ON a2.DNI = p.DNI
WHERE a.COSTEBASICO > 4.5;


-- 14
