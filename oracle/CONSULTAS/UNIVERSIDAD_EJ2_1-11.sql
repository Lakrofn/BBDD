-- 6
SELECT P.DNI, P.NOMBRE, P.APELLIDO FROM PERSONA p JOIN ALUMNO a 
ON p.DNI = a.DNI
JOIN ALUMNO_ASIGNATURA aa
ON a.IDALUMNO = aa.IDALUMNO
JOIN ASIGNATURA a2
ON aa.IDASIGNATURA = a2.IDASIGNATURA
WHERE UPPER(a2.NOMBRE) LIKE 'SEGURIDAD VIAL';

-- 7
SELECT A.IDTITULACION FROM ASIGNATURA A JOIN ALUMNO_ASIGNATURA aa 
ON A.IDASIGNATURA = aa.IDASIGNATURA
JOIN ALUMNO a2
ON aa.IDALUMNO = a2.IDALUMNO
JOIN PERSONA p 
ON a2.DNI = p.DNI
WHERE P.DNI LIKE '20202020A';

-- 8
SELECT A.NOMBRE  FROM ASIGNATURA A JOIN ALUMNO_ASIGNATURA aa
ON A.IDASIGNATURA = aa.IDASIGNATURA
JOIN ALUMNO a2 
ON aa.IDALUMNO = a2.IDALUMNO
JOIN PERSONA p 
ON a2.DNI = p.DNI
WHERE p.NOMBRE LIKE 'Rosa' AND p.APELLIDO LIKE 'Garcia';

-- 9
SELECT A.DNI FROM ALUMNO A JOIN ALUMNO_ASIGNATURA aa 
ON A.IDALUMNO = aa.IDALUMNO
JOIN ASIGNATURA a2 
ON aa.IDASIGNATURA = a2.IDASIGNATURA
JOIN PROFESOR p 
ON a2.IDPROFESOR = p.IDPROFESOR
JOIN PERSONA p2 
ON p.DNI = p2.DNI
WHERE p2.NOMBRE LIKE 'Jorge' AND p2.APELLIDO LIKE 'Saenz';

-- 10
SELECT p2.dni, p2.nombre, p2.apellido FROM persona p JOIN profesor pr 
ON p.dni = pr.dni 
JOIN asignatura a 
ON pr.idprofesor = a.idprofesor
JOIN alumno_asignatura aa 
ON a.idasignatura = aa.idasignatura
JOIN alumno al 
ON aa.idalumno = al.idalumno
