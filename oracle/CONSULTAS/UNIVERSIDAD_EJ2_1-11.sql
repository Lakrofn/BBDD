SELECT A.NOMBRE, A.COSTEBASICO FROM TITULACION T RIGHT JOIN ASIGNATURA A
ON A.IDASIGNATURA = T.IDTITULACION
WHERE A.IDTITULACION IS NOT NULL
ORDER BY A.COSTEBASICO DESC, A.NOMBRE ASC;

