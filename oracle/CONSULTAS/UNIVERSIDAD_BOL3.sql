-- 7
SELECT P.NOMBRE,
SUM(A.COSTEBASICO * (1 + (AA.NUMEROMATRICULA-1)*0.10)) PAGA FROM PERSONA P
JOIN ALUMNO AL 
ON P.DNI = AL.DNI
JOIN ALUMNO_ASIGNATURA AA 
ON AL.IDALUMNO = AA.IDALUMNO
JOIN ASIGNATURA A 
ON AA.IDASIGNATURA = A.IDASIGNATURA
GROUP BY P.NOMBRE;

--8
SELECT T.NOMBRE, AVG(A.COSTEBASICO) FROM TITULACION T
JOIN ASIGNATURA A
ON T.IDTITULACION = A.IDTITULACION
GROUP BY T.NOMBRE
HAVING SUM(A.COSTEBASICO) > 60;

--9
SELECT T.NOMBRE FROM TITULACION T
JOIN ASIGNATURA A 
ON T.IDTITULACION = A.IDTITULACION
JOIN ALUMNO_ASIGNATURA AA 
ON A.IDASIGNATURA = AA.IDASIGNATURA
GROUP BY T.NOMBRE
HAVING COUNT(DISTINCT AA.IDALUMNO) > 3;

