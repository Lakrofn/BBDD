-- 12
SELECT P.NOMBRE, COUNT(DISTINCT AA.IDALUMNO) FROM PERSONA P
JOIN PROFESOR PR 
ON P.DNI = PR.DNI
JOIN ASIGNATURA A 
ON PR.IDPROFESOR = A.IDPROFESOR
JOIN ALUMNO_ASIGNATURA AA 
ON A.IDASIGNATURA = AA.IDASIGNATURA
GROUP BY P.NOMBRE
HAVING COUNT(DISTINCT AA.IDALUMNO) >= 2;

