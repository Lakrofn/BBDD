CREATE TABLE CICLISTA
(
	NOMBRECICLISTA VARCHAR2(80),
	FNACIMIENTO DATE,
	NACIONALIDAD VARCHAR2(20),
	CONSTRAINT PK_CICLISTA PRIMARY KEY (NOMBRECICLISTA)
);
CREATE TABLE EQUIPO
(
	NOMBREEQUIPO VARCHAR2(80),
	CONSTRAINT PK_EQUIPO PRIMARY KEY (NOMBREEQUIPO)
);
CREATE TABLE PRUEBA
(
	NOMBREPRUEBA VARCHAR2(80),
	NOMBRECICLISTA_GANADOR VARCHAR2(80),
	ANIO DATE,
	KMTOTALES NUMBER(10),
	CONSTRAINT PK_PRUEBA PRIMARY KEY (NOMBREPRUEBA,NOMBRECICLISTA_GANADOR),
	CONSTRAINT FK1_PRUEBA FOREIGN KEY (NOMBRECICLISTA_GANADOR) REFERENCES CICLISTA(NOMBRECICLISTA)
);
CREATE TABLE PERTENECE
(
	NOMBRECICLISTA VARCHAR2(80),
	NOMBREEQUIPO VARCHAR2(80),
	CONSTRAINT PK_PERTENECE PRIMARY KEY (NOMBRECICLISTA, NOMBREEQUIPO),
	CONSTRAINT FK1_PERTENECE FOREIGN KEY (NOMBRECICLISTA) REFERENCES CICLISTA(NOMBRECICLISTA),
	CONSTRAINT FK2_PERTENECE FOREIGN KEY (NOMBREEQUIPO) REFERENCES EQUIPO(NOMBREEQUIPO)
	
);

