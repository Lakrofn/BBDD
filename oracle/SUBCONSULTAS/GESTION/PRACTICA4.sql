FROM ARTICULOS a 
WHERE A.STOCK >20 AND A.PRECIO >15 
AND A.CODART NOT IN                   
		(SELECT CODART
		FROM LINEAS_FAC LF
		JOIN FACTURAS F ON F.CODFAC = LF.CODFAC 
		WHERE EXTRACT(YEAR FROM FECHA) = 
			  EXTRACT(YEAR FROM SYSDATE)-1
		AND	
			  EXTRACT (MONTH FROM FECHA) IN (10,11,12))

-- 7
SELECT COUNT(*)
FROM CLIENTES C
WHERE NOT EXISTS (
    SELECT 1 FROM FACTURAS F
    WHERE F.CODCLI = C.CODCLI
    AND EXTRACT(YEAR FROM F.FECHA) = EXTRACT(YEAR FROM SYSDATE)-1
    AND (F.IVA = 0 OR F.IVA IS NULL)
);

-- 8 (HECHO CON AYUDA)
SELECT C.CODCLI, C.NOMBRE
FROM CLIENTES C
JOIN FACTURAS f 
ON C.CODCLI = f.CODCLI
JOIN LINEAS_FAC lf 
ON f.CODFAC = lf.CODFAC
WHERE (
    SELECT SUM(LF.CANT * LF.PRECIO) FROM FACTURAS F
    WHERE F.CODCLI = C.CODCLI
    AND EXTRACT(MONTH FROM F.FECHA) = 11
    AND EXTRACT(YEAR FROM F.FECHA) = EXTRACT(YEAR FROM SYSDATE)-1
) > 60.50 AND NOT EXISTS (
    SELECT 1 FROM FACTURAS F
    	WHERE F.CODCLI = C.CODCLI
    AND EXTRACT(MONTH FROM F.FECHA) = 12
    AND EXTRACT(YEAR FROM F.FECHA) = EXTRACT(YEAR FROM SYSDATE)-1
);

