SELECT f.FECHA FROM FACTURAS f 
JOIN LINEAS_FAC lf 
ON f.CODFAC = lf.CODFAC
WHERE lf.CANT * lf.PRECIO >= (SELECT MAX(lf2.PRECIO * lf2.CANT) FROM LINEAS_FAC lf2);

SELECT f.FECHA FROM FACTURAS f 
JOIN LINEAS_FAC lf 
ON f.CODFAC = lf.CODFAC
GROUP BY f.FECHA 
ORDER BY SUM(lf.PRECIO * lf.CANT) DESC
FETCH FIRST ROW ONLY;

