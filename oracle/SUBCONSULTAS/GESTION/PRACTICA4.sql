FROM ARTICULOS
WHERE PRECIO > 3000 * (SELECT MIN(PRECIO) FROM ARTICULOS);

-- 13
SELECT C.NOMBRE FROM CLIENTES C
JOIN FACTURAS F 
ON C.CODCLI = F.CODCLI
JOIN LINEAS_FAC LF 
ON F.CODFAC = LF.CODFAC
GROUP BY C.NOMBRE
ORDER BY SUM(LF.CANT * LF.PRECIO) DESC
FETCH FIRST 1 ROW ONLY;

