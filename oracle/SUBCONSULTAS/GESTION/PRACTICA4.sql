SELECT a.CODART , a.DESCRIP, a.PRECIO  FROM ARTICULOS a
ORDER BY a.PRECIO DESC
FETCH FIRST 10 ROWS ONLY;

-- 10
SELECT PR.NOMBRE FROM PROVINCIAS PR
JOIN PUEBLOS PU 
ON PR.CODPRO = PU.CODPRO
JOIN CLIENTES C 
ON PU.CODPUE = C.CODPUE
GROUP BY PR.NOMBRE
ORDER BY COUNT(*) DESC
FETCH FIRST 1 ROW ONLY;

-- 12
